package chapter02;

public class ArithTest01 {

	public static void main(String[] args) {
		//화몷 錢챹 狗
		
		int mathScore=96;
		int engScore=87;
		float total=(mathScore+engScore);
	
		
		
		//화
		System.out.println("화 : "+(mathScore+engScore)); // 화:183
		
		//錢
		System.out.println("錢 : "+(total/2)); //錢:91

	}

}
